package operator;

// 연산자 : 데이터를 가공하여 새로운 데이터를 얻는 기호들
// 종류 : 산술, 대입, 증감, 비교, 논리, 조건

public class Ex01 {
	public static void main(String[] args) {
		// 비교(관계) 연산자 : 데이터의 대소 관계를 비교하는 연산자
		// 종류 : <, >, <=, >=, ==, !=
		
		System.out.println("10 < 5 결과 : " + (10 < 5));
		
		System.out.println("10.1 > 10 결과 : " + (10.1 >10));
		
		System.out.println("10 == 10 결과 : " + (10 == 10));
		System.out.println("10 != 10 결과 : " + (10 != 10));
		
		
	}
}
























