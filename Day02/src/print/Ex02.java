package print;

public class Ex02 {

	public static void main(String[] args) {
		// escape : 탈출하다, 벗어나다
		
		// escape sequence : 이스케이프 시퀀스 
		// - 문자열 내에서 특수한 형태로 사용되는 문자들을 의미
		// - \(백슬래시)와 같이 결합되어 사용된다.
		
		System.out.println("Hello\nworld!!!");
		
		// escape는 문자열 내에서 몇번이건 사용가능
		System.out.println("\n버전은\nJDK1.8\n도구는\neclipse-2020-03\n");
		
		System.out.println("여기는\t 엄청\t\t 띄워집니다~\n"); // \t : tab, 들여쓰기
		
		System.out.println("저희 강의실은 \"1102호\"입니다");		// \" : 문자열 묶는 기능을 제거
		
		System.out.println("공유 폴더는 \\192.168.0.37");		// \\ : 이스케이프 기능을 제거
		
		
		
		
		
		
		
		
	}
}







































