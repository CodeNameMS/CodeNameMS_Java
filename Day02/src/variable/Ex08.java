package variable;

public class Ex08 {
	public static void main(String[] args) {
		// 문자열은 기본 자료형이 없다.
		
		String name = "홍길동";
		System.out.println("name = " + name);
		
		// String은 기본자료형이 아닌 '클래스'라고 한다.
		// 자바에서 문자열 처리를 좀  더 수월하게 하기 위해서 제공하는 클래스 타입
		
		int age = 25;
		System.out.println("age = " + age);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
